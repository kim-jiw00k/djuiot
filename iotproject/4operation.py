#사칙 연산

print("5 + 7 =",5 + 7)      #덧셈
print("5 - 7 =",5 - 7)      #뺄셈
print("5 * 7 =",5 * 7)      #곱셈
print("5 / 7 =",5 / 7)      #나눗셈

print("3 / 2 =",3 / 2)      #나눗셈 소수점 나옴
print("3 // 2 =",3 // 2)    #소수점 이하의 자릿수 삭제한 후 정수 부분만 남김
print("5 % 2 =",5 % 2)      #A를 B 로 나누었을때 나머지를 구함

#제곱 연산자
print("2 ** 1 =", 2 ** 1)
print("2 ** 2 =", 2 ** 2)
print("2 ** 3 =", 2 ** 3)
print("2 ** 4 =", 2 ** 4)